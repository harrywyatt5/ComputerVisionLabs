package Week3;

public class Peak {
    private int index;
    private float value;

    public Peak(int i, float v) {
        index = i;
        value = v;
    }

    public int getIndex() {
        return index;
    }

    public float getValue() {
        return value;
    }

    public void setIndex(int i) {
        index = i;
    }

    public void setValue(float v) {
        value = v;
    }
}
